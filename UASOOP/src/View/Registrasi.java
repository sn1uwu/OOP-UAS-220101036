    private void tampilData(){
        try{
            int baris = tablePengguna.getRowCount();
            for(int i = 0; i < baris; i++) {
                tabelDataPengguna.delete(0, baris);
            }
            String sql = "SELECT * FROM pengguna";
            ResultSet rs = conn.getData(sql);
            while(rs.next()){
                Pengguna pengguna = new Pengguna();
                pengguna.setUsername(rs.getString(1));
                pengguna.setNoHP(rs.getString(2));
                pengguna.setAlamatPengguna(rs.getString(3));
                pengguna.setEmail(rs.getString(4));
                pengguna.setKataSandi(rs.getString(5));
                tabelDataPengguna.add(pengguna);
            }
        } catch (SQLException ex) {
            System.err.print(ex);
        }
    }
    
