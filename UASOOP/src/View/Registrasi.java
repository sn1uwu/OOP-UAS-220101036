    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String username = textUsername.getText();
        String nohp = textHP.getText();
        String alamat = textAlamat.getText();
        String email = textEmail.getText();
        String password = textPassword.getText();
        if ((username.isEmpty()) | (nohp.isEmpty()) | (alamat.isEmpty()) 
                | (email.isEmpty()) | (password.isEmpty())){
            JOptionPane.showMessageDialog(null, "data tidak boleh kosong, " +
                "silahkan dilengkapi");
            textUsername.requestFocus();
        }else {
            try {
                String sql = "INSERT INTO `pengguna` (`username`, `noHP`, "
                        + "`alamatPengguna`, `email`, `kataSandi`, `create_date`) "
                        + "VALUES ("
                        + "'" + username + "',"
                        + "'" + nohp + "',"
                        + "'" + alamat + "',"
                        + "'" + email + "',"
                        + "'" + password + "',"
                        + null 
                + ")";
                conn.query(sql);
                tampilData();
                JOptionPane.showMessageDialog(null, username + " Berhasil Disimpan");
                clearText();
                SetEditOff();
                btnNew.setEnabled(true);
            }catch (Exception ex) {
                System.err.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

